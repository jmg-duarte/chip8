const WIDTH: u8 = 64;
const HEIGHT: u8 = 32;

pub struct Display {
    pub memory: [bool; 2048],
}

impl Display {
    pub fn new() -> Display {
        Display {
            memory: [false; 2048],
        }
    }

    pub fn cls(&mut self) {
        for x in 0..WIDTH {
            for y in 0..HEIGHT {
                self.memory[(x + y * WIDTH) as usize] = false
            }
        }
    }

    pub fn set_pixel(&mut self, x: u8, y: u8, v: bool) {
        self.memory[(x + y * WIDTH) as usize] = v
    }

    pub fn get_pixel(&self, x: u8, y: u8) -> bool {
        self.memory[(x + y * WIDTH) as usize]
    }
}

